package com.walgreens.batch.central.bean;

/**
 * @author CTS
 *
 */
public class KronosDataBean {
	
	private String units;
	private String storeNumber;
	private String matrixCode;
	private String date;
	private String dayName;
	private String slotAndCount;
	/**
	 * @return the units
	 */
	public String getUnits() {
		return units;
	}
	/**
	 * @param units the units to set
	 */
	public void setUnits(String units) {
		this.units = units;
	}
	/**
	 * @return the storeNumber
	 */
	public String getStoreNumber() {
		return storeNumber;
	}
	/**
	 * @param storeNumber the storeNumber to set
	 */
	public void setStoreNumber(String storeNumber) {
		this.storeNumber = storeNumber;
	}
	/**
	 * @return the matrixCode
	 */
	public String getMatrixCode() {
		return matrixCode;
	}
	/**
	 * @param matrixCode the matrixCode to set
	 */
	public void setMatrixCode(String matrixCode) {
		this.matrixCode = matrixCode;
	}
	/**
	 * @return the date
	 */
	public String getDate() {
		return date;
	}
	/**
	 * @param date the date to set
	 */
	public void setDate(String date) {
		this.date = date;
	}
	/**
	 * @return the slotAndCount
	 */
	public String getSlotAndCount() {
		return slotAndCount;
	}
	/**
	 * @param slotAndCount the slotAndCount to set
	 */
	public void setSlotAndCount(String slotAndCount) {
		this.slotAndCount = slotAndCount;
	}
	/**
	 * @return the dayName
	 */
	public String getDayName() {
		return dayName;
	}
	/**
	 * @param dayName the dayName to set
	 */
	public void setDayName(String dayName) {
		this.dayName = dayName;
	}
}
