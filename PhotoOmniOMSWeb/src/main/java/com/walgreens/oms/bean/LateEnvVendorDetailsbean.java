package com.walgreens.oms.bean;

public class LateEnvVendorDetailsbean {
	private String description;
	private String phone;
	private String status;
	private String shipmentUrl;
	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}
	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}
	/**
	 * @return the phone
	 */
	public String getPhone() {
		return phone;
	}
	/**
	 * @param phone the phone to set
	 */
	public void setPhone(String phone) {
		this.phone = phone;
	}
	/**
	 * @return the status
	 */
	public String getStatus() {
		return status;
	}
	/**
	 * @param status the status to set
	 */
	public void setStatus(String status) {
		this.status = status;
	}
	/**
	 * @return the shipmentUrl
	 */
	public String getShipmentUrl() {
		return shipmentUrl;
	}
	/**
	 * @param shipmentUrl the shipmentUrl to set
	 */
	public void setShipmentUrl(String shipmentUrl) {
		this.shipmentUrl = shipmentUrl;
	}
	
	

}
